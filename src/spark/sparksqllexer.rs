fn isValidDecimal<'input, Input: CharStream<From<'input>>>(input: &mut Input) -> bool {
	let nextChar = fromCodePoint(input.la(1));
	!(nextChar >= 'A' && nextChar <= 'Z' || nextChar >= '0' && nextChar <= '9' || nextChar == '_')
}

fn fromCodePoint(codePoint: isize) -> char {
	char::from_u32(codePoint as u32).expect("Invalid Char")
}

fn isHint<'input, Input: CharStream<From<'input>>>(input: &mut Input) -> bool {
	let nextChar = fromCodePoint(input.la(1));
	nextChar == '+'
}

